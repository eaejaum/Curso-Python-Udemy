# TIPO STR (string)

"""
Python = Linguagem de programação
Tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentrp de aspas
"""

print(1234)

# Aspas simples
print('João Pedro')
print(1, 'João "Pedro"')


# Aspas duplas
print("João Pedro")
print(2, "João 'Pedro'")


# Escape
print("João \"Pedro\"")

# r
print(r"João \"Pedro\"")